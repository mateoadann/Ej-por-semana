# 1. División con resto
# Plantear un script (directamente en el shell de Python) que permita informar,
# para dos valores a y b el resultado de la división a/b y el resto de esa división.

# Dates:
a = 10
b = 3
# Process
division = a/b
resto = a % b
# Results
